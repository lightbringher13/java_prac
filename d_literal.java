class Literal{
    public static void main(String a[]){
        int num1 = 585;
        int num2 = 0b101;
        int num3 = 0x7E;
        int num4 = 10_000_000;
        double num5 = 12e10;
        boolean num6 = true;
        char c = 'a';
        c++;
        
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);
        System.out.println(c);
    }
}